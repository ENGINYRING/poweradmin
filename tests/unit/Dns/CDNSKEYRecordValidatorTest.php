    public function testValidateWithInvalidAlgorithm()
    {
        $content = '257 3 20 mdsswUyr3DPW132mOi8V9xESWE8jTo0dxCjjnopKl+GqJxpVXckHAeF+KkxLbxILfDLUT0rAK9iUzy1L53eKGQ==';
        $name = 'host.example.com';
        $prio = 0;
        $ttl = 3600;
