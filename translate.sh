# Function to update a translation in a PO file
update_translation() {
  locale="$1"
  msgid="$2"
  msgstr="$3"
  po_file="locale/$locale/LC_MESSAGES/messages.po"
  
  if [ \! -f "$po_file" ]; then
    echo "Warning: PO file for locale '$locale' not found. Skipping."
    return
  fi
  
  # Escape special characters for sed
  esc_msgid=$(echo "$msgid" | sed 's/[\/&]/\\&/g')
  esc_msgstr=$(echo "$msgstr" | sed 's/[\/&]/\\&/g')
  
  # Temporary file
  temp_file=$(mktemp)
  
  # Find the line with msgid and replace the msgstr line
  awk -v msgid="$esc_msgid" -v msgstr="$esc_msgstr" '
    /msgid "'"$esc_msgid"'"/ {
      print;
      getline;
      if ($0 ~ /^msgstr/) {
        print "msgstr \"" msgstr "\"";
      } else {
        print;
      }
      next;
    }
    
    # Remove auto-english-fallback for this msgid
    /^#, auto-english-fallback/ {
      getline;
      if ($0 ~ /msgid "'"$esc_msgid"'"/) {
        print $0;
        next;
      } else {
        print "#, auto-english-fallback";
        print $0;
      }
      next;
    }
    
    # Print all other lines
    { print; }
  ' "$po_file" > "$temp_file"
  
  # Replace the original file
  mv "$temp_file" "$po_file"
  
  echo "Updated translation for '$msgid' in $locale"
}

# Function to compile a PO file to MO
compile_po() {
  locale="$1"
  po_file="locale/$locale/LC_MESSAGES/messages.po"
  mo_file="locale/$locale/LC_MESSAGES/messages.mo"
  
  if [ \! -f "$po_file" ]; then
    echo "Warning: PO file for locale '$locale' not found. Skipping compilation."
    return
  fi
  
  msgfmt -o "$mo_file" "$po_file"
  echo "Compiled $locale ($po_file -> $mo_file)"
}

